"""zall.cli.init_wizard — Interactive /init project setup wizard (P3-b).

Replaces the old 4-file template generator with an interactive Q&A flow
that asks about project type, tech stack, testing, MCP servers, and model.

Uses prompt_toolkit for interactive prompts (falls back to plain input).
"""

from __future__ import annotations

import sys
from pathlib import Path
from typing import Any

try:
    from prompt_toolkit import prompt as ptk_prompt
    from prompt_toolkit.shortcuts import yes_no_dialog, radiolist_dialog, input_dialog
    _HAS_PTK = True
except ImportError:
    _HAS_PTK = False


# ──────────────────────────────────────────────────────────────────────────
# Template generators
# ──────────────────────────────────────────────────────────────────────────


def _rules_toml(project_type: str, test_framework: str) -> str:
    """Generate rules.toml based on project type."""
    deny_commands = ["rm -rf", "git push --force", "git reset --hard"]
    if project_type in ("python",):
        deny_commands.append("pip uninstall")
    elif project_type in ("rust",):
        deny_commands.append("cargo uninstall")

    # 渲染 TOML 数组 (非 Python repr — 避免 B1 臭名昭著的 f-string list bug)
    deny_commands_toml = "[" + ", ".join(f'"{c}"' for c in deny_commands) + "]"

    test_comment = ""
    if test_framework:
        test_comment = f"# Test framework: {test_framework}"
        if test_framework == "pytest":
            test_comment += "\n# pytest run: `python -m pytest tests/`"

    return f"""\
# zall project rules (§4.2.1 context_judge — user-local Authority rules)
# Auto-generated by `zall init` (project type: {project_type})

[allow]
# Tools/actions that skip confirmation (whitelist)
# WARNING: only add trusted, read-only tools here
tools = ["read_file", "list_dir", "grep", "glob"]

[deny]
# Tools/actions that require explicit override + audit
tools = []
commands = {deny_commands_toml}

[settings]
# Project-level default judge mode
#   none   = undecidable (default, honest fallback)
#   system = run tests to determine task completion
judge = "none"

[compactor]
# Context compression settings (optional)
# strategy: "model_summary" | "truncation"
# threshold: minimum messages before compaction
strategy = "model_summary"
threshold = 8
"""


def _agents_md(project_type: str, description: str) -> str:
    """Generate AGENTS.md with project-specific guidance."""
    tech_stack_hints = {
        "python": "Python (>=3.10), pytest, ruff, mypy",
        "javascript": "Node.js, npm/yarn/pnpm, Jest/Vitest",
        "typescript": "TypeScript, Node.js, npm/yarn/pnpm, Jest/Vitest",
        "rust": "Rust, cargo, rustfmt, clippy",
        "go": "Go, gofmt, go mod",
        "generic": "",
    }
    hint = tech_stack_hints.get(project_type, "")

    return f"""\
# AGENTS.md — zall project memory (§9.4)
#
# > Project-level memory: agent can READ this; modifying it requires
# > greylist confirmation (confirm_gate + audit).
# > The agent must NEVER modify Authority rules via AGENTS.md.

## Project Overview
{description or f"A {project_type} project"}

## Tech Stack
{hint or "(describe your tech stack here)"}

## Conventions
- Code style: (describe formatting / linting rules)
- Testing: (describe test conventions)
- Branch strategy: (main, develop, feature branches)
"""


