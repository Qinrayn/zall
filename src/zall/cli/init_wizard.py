def _ask_ptk(label: str, default: str = "") -> str:
    """Use prompt_toolkit for interactive input."""
    try:
        return ptk_prompt(f"  {label}: ", default=default)
    except (EOFError, KeyboardInterrupt):
        return default or ""


def _ask_plain(label: str, default: str = "") -> str:
    """Fallback to plain input()."""
    try:
        val = input(f"  {label} [{default}]: ").strip()
        return val if val else default
    except (EOFError, KeyboardInterrupt):
        return default or ""


def init_wizard(out: Any, cwd: str | None = None) -> None:
    """Interactive project initialization wizard (P3-b).

    Replaces the old _cmd_init 4-file template generator with a
    Q&A flow that asks about project type, tech stack, and preferences.
    """
    base = (Path(cwd) if cwd else Path.cwd()) / ".zall"
    if base.exists() and any(base.iterdir()):
        out.write("  .zall/ already exists and is not empty.\n")
        out.write("  Delete .zall/ first if you want to reinitialize.\n")
        return

    base.mkdir(parents=True, exist_ok=True)
    ask = _ask_ptk if _HAS_PTK else _ask_plain

    out.write("  zall project setup wizard\n")
    out.write("  (press Enter to accept defaults)\n\n")

    # ── Step 1: Project type ──
    project_type = ask("Project type", "python")
    if not project_type:
        project_type = "python"
    project_type = project_type.strip().lower()

    # ── Step 2: Description ──
    description = ask("Short project description", "My zall project")

    # ── Step 3: Test framework ──
    test_hints = {
        "python": "pytest",
        "javascript": "jest",
        "typescript": "jest",
        "rust": "cargo test",
        "go": "go test",
    }
    default_test = test_hints.get(project_type, "")
    test_framework = ask("Test framework", default_test)

    # ── Step 4: Model preference ──
    model = ask("Default model (e.g., gpt-4o, claude-sonnet-4)", "gpt-4o")

    # ── Step 5: Write files ──
    files_created = []

    # rules.toml
    rules_content = _rules_toml(project_type, test_framework)
    (base / "rules.toml").write_text(rules_content, encoding="utf-8")
    files_created.append("rules.toml")

    # AGENTS.md
    agents_content = _agents_md(project_type, description)
    (base / "AGENTS.md").write_text(agents_content, encoding="utf-8")
    files_created.append("AGENTS.md")

    # mcp.toml
    (base / "mcp.toml").write_text(_mcp_toml(), encoding="utf-8")
    files_created.append("mcp.toml")

    # skills.toml
    (base / "skills.toml").write_text(_skills_toml(), encoding="utf-8")
    files_created.append("skills.toml")

    # config.toml (persist model choice)
    config_path = Path.home() / ".zall" / "config.toml"
    config_path.parent.mkdir(parents=True, exist_ok=True)
    config_content = f"""[auth]
# API key (or set ZALL_API_KEY env var)
api_key = ""

[model]
	name = "{model}"

[settings]
stream = true
verbose = false
"""
    config_path.write_text(config_content, encoding="utf-8")
    files_created.append("~/.zall/config.toml")

    # O5: TrustAnchor init化 (生成 ed25519 key + anchor指纹)
    try:
        from zall.core.verifiability import FileTrustAnchor
        anchor = FileTrustAnchor()
        if anchor.anchor_id:
            out.write(f"  ✓ trust anchor initialized (id={anchor.anchor_id})\n")
            files_created.append("~/.zall/trust_anchor_key (ed25519)")
    except Exception as e:
        out.write(f"  · trust anchor setup skipped: {e}\n")

    # ── Summary ──
    out.write(f"\n  ✓ Initialized zall project config at {base}\n")
    for f in files_created:
        if f.startswith("~/"):
            out.write(f"    - {f}\n")
        else:
            out.write(f"    - .zall/{f}\n")
    out.write(f"\n  Project: {project_type.upper()}")
    if test_framework:
        out.write(f"  |  Test: {test_framework}")
    out.write(f"  |  Model: {model}\n")
    out.write("\n  Run 'zall' to start working.\n")